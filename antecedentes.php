				<h1>Antecedentes</h1>
			<p>

La Red de Planes Ambientales  Institucionales (PAIs)  se constituyó en el marco del Consejo Regional Sur Sureste de la ANUIES, teniendo sus antecedentes en la XIII Reunión Ordinaria del Consejo de Universidades Públicas e Instituciones Afines (CUPIA) realizada en el Instituto Tecnológico de Sonora en 1999, durante la cual fue presentado el documento elaborado conjuntamente por el Centro de Educación y Capacitación para el Desarrollo Sustentable (CECADESU/SEMARNAT) y la Secretaría General de la ANUIES, que contenía la propuesta para el establecimiento de un Plan de Acción para el Desarrollo Sustentable en las Instituciones de Educación Superior. A partir de la aprobación de dicha propuesta se convocó a las universidades el país a remitir información sobre las actividades que en materia ambiental estaban desarrollando en ese momento, con el objeto de conocer el diagnóstico de las IES en materia ambiental y definir una propuesta que orientara y fortaleciera el trabajo ambiental de las mismas.</p>
<p>Posteriormente, el CECADESU-SEMARNAT y la ANUIES organizaron en el año 2002, el Curso Plan de Acción para el Desarrollo Sustentable en las Instituciones de Educación Superior y el Curso-Taller: Formación Ambiental para la elaboración de Planes Ambientales Institucionales, el primero en el Instituto Tecnológico de Minatitlán, Veracruz y el segundo, en el Instituto Tecnológico de Campeche, con cuya metodología se elaboraron los planes ambientales institucionales, mientras aquellas que ya lo tenían fortalecieron su Plan.</p>
<p>El 18 de febrero de 2005, siendo sede la Universidad Veracruzana, la ANUIES convocó a sus instituciones miembros de la región sur-sureste de México a la Reunión Regional para la Creación de Nuevas Redes. En ésta reunión quedó conformada la Red de Programas Ambientales Institucionales “Desarrollo sustentable”, en la que estuvieron representantes de 15 universidades e institutos: Universidad Autónoma de Campeche, Universidad Autónoma del Carmen, Colegio de la Frontera Sur, Universidad de Ciencias y Artes de Chiapas, Universidad Autónoma de Chiapas, Instituto Tecnológico de Oaxaca, Instituto Tecnológico de Tuxtepec, Universidad Benito Juárez de Oaxaca, Universidad de Quintana Roo, Universidad Juárez Autónoma de Tabasco, Universidad Veracruzana, Universidad Autónoma de Yucatán, Instituto Tecnológico de Veracruz y Universidad Cristóbal Colón.</p>
<p>Durante la reunión, la Universidad Autónoma de Campeche presentó la iniciativa para la conformación de la Rd, misma que fue revisada y analizada por las diversas instituciones; dicho documento fue presentado posteriormente al Consejo Regional Sur-Sureste de la ANUIES, nombrándose a la Mtra. Miriam Sahagún Arcila como enlace temporal de la red y a la Dra. Silvia Ramos Hernández, Mtro. José Manuel Asún, Q.F.B. Roger Medina González a la Dra. Socorro Menchaca Dávila como responsables de los programas básicos. La terna para definir al responsable de coordinar los trabajos de la Red estuvo integrada por la Mtra. Miriam Sahagún, la Dra. Silvia Ramos y la Dra. Socorro Menchaca, eligiéndose a la Mtra. Sahagún como primera Coordinadora de la Red.</p>
<p>El 18 de abril de 2005, durante la Primera Sesión Ordinaria 2005 del Consejo Regional Sur-Sureste de la ANUIES, en la Sala de Usos Múltiples “La Laguna” de la Universidad Juárez Autónoma de Tabasco, la Universidad Autónoma de Campeche, por conducto de la Mtra. Miriam Sahagún Arcila (UAC), se presentó la propuesta para la creación de la Red-PAI, evento que estuvo presidido por el Dr. Jorge Luis Ibarra Mendivil, el entonces Secretario General Ejecutivo de la ANUIES y la M. en C. Enna A. Sandoval Castellanos, M.C., entonces Presidenta del Consejo Regional Sur-Sureste de la ANUIES, asistiendo rectores y representantes de rectores de los estados de la región sur-sureste, así como el Gobernador del Estado de Tabasco. En esta reunión se presentaron las propuestas para la creación de las redes de Vinculación, Tutorías, Posgrado e Investigación, Equidad y Género y Planes Ambientales Institucionales, siendo aprobada la creación de la Red PAI, quedando como primera instancia coordinadora de la Red PAIs, la Universidad Autónoma de Campeche, por conducto de la Mtra. Miriam Sahagún Arcila.</p>
