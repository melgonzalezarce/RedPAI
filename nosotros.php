				<h1>¿Quiénes somos?</h1>
			<p>

Nuestra estructura está enmarcada en los Lineamientos Generales  para la operación de las Redes Interinstitucionales de Cooperación del Consejo regional sur sureste de la ANUIES (Marzo, 2009). Subrayamos los artículos en los que se basa la operación de nuestra Red:</p>

<p> <strong>Artículo 1:</strong> El Consejo Regional Sur Sureste, con fundamento en el Artículo 26º y la fracción I del Artículo 23º del Estatuto de la Asociación Nacional de Universidades e Instituciones de Educación Superior de la República Mexicana (ANUIES), emite los siguientes lineamientos para el establecimiento, integración y operación de las Redes Interinstitucionales de Cooperación que de común acuerdo han establecido y establezcan en el futuro las instituciones asociadas, para coadyuvar en el mejor cumplimiento de sus objetivos y actividades, particularmente, para “Promover el mejoramiento de las funciones de docencia, investigación, difusión y extensión de la cultura, así como las de apoyo administrativo en las instituciones de la región, en atención a las características, condiciones y problemas regionales,” como lo establece la fracción V del artículo 23º del citado estatuto. </p>
<p><strong>Artículo 2:</strong> Se entiende por Red Interinstitucional de Cooperación, el conjunto organizado de académicos, investigadores y/o administradores de Instituciones de Educación Superior (IES) miembros del Consejo Regional, expresamente designados por los titulares de las diferentes instituciones asociadas, en las áreas académicas y administrativas, que de común acuerdo se establezcan y orientadas a fortalecer la cooperación interinstitucional y mejorar la calidad de la educación superior. </p>
<p><strong>Artículo 3:</strong> Las Redes tendrán por objeto contribuir en el desarrollo de proyectos y actividades específicas acordadas por las Instituciones de Educación Superior de nuestra Región, para que, de acuerdo con sus propias características, promuevan y fomenten, también, el intercambio de conocimiento y experiencias, estableciendo mecanismos de comunicación y gestión interinstitucional con sus pares y con organizaciones locales, regionales, nacionales e internacionales que persigan objetivos afines. Las Redes Interinstitucionales de Cooperación se constituirán con objetivos y encomiendas específicas y serán disueltas al cumplirse su cometido. </p> 
<p><strong>Artículo 4:</strong> Para el cumplimiento de los objetivos antes mencionados, las Redes, en sus respectivas áreas de conocimiento, disciplinas o campos de trabajo, elaborarán y desarrollarán sus proyectos y programas de trabajo, procurando que contribuyan al desarrollo institucional en alguna o varias de las áreas que a continuación se enumeran con carácter ejemplificativo, más no exhaustivo:
<ul>
<li>Armonización de los programas de estudio que se imparten en las IES de la Región; </li>
<li>Desarrollo de metodologías para la aplicación del SATCA a los programas concretos. </li>
<li>Identificación de debilidades y fortalezas como insumo para la cooperación entre IES </li>
<li>Programas académicos compartidos de educación a distancia </li>
<li>Fortalecimiento de áreas estratégicas que se constituyan como un núcleo promotor para extender los beneficios a otras universidades e instituciones de educación superior de la Región. </li>
<li>Uso compartido de infraestructura, tanto en espacios fijos como de sistemas de información, programas de movilidad e investigación y desarrollo tecnológico para la innovación educativa. </li>
<li>Establecimiento y desarrollo del espacio Común de Educación Superior de la Región Sur Sureste; </li>
<li>Estrategia de cooperación y movilidad universitaria de carácter horizontal con el objetivo de posibilitar la creación y potenciación de las capacidades académicas de las IES de la región sur-sureste de la ANUIES para lograr un mayor impacto y pertinencia social. </li>
<li>Proceso de internacionalización de las IES. </li>
<li>Mejora de los canales de comunicación interinstitucional y de los sistemas administrativos. </li>
<li>Integración Universitaria para el Desarrollo Humano y Sustentable de la Región Sur-Sureste de la ANUIES </li>
</ul>
</p>

